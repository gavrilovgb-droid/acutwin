// Parse inline **bold** and `code`
function ri(text) {
  const out = [];
  const re = /\*\*(.*?)\*\*|`(.*?)`/g;
  let i = 0, m;
  while ((m = re.exec(text))) {
    if (m.index > i) out.push(new TextRun({ text: text.slice(i, m.index) }));
    if (m[1] != null) out.push(new TextRun({ text: m[1], bold: true }));
    else out.push(new TextRun({ text: m[2], font: 'Courier New', size: 20 }));
    i = re.lastIndex;
  }
  if (i < text.length) out.push(new TextRun({ text: text.slice(i) }));
  return out.length ? out : [new TextRun({ text })];
}

const sp = (b, a) => ({ spacing: { before: b, after: a } });

const h1 = t => new Paragraph({ heading: HeadingLevel.HEADING_1, children: [new TextRun(t)], ...sp(0, 200) });
const h2 = (t, theme) => new Paragraph({ heading: HeadingLevel.HEADING_2, children: [new TextRun(t)], ...sp(300, 120) });
const h3 = t => new Paragraph({ heading: HeadingLevel.HEADING_3, children: [new TextRun(t)], ...sp(200, 80) });
const p  = t => new Paragraph({ children: ri(t), ...sp(60, 60) });
const b  = t => new Paragraph({ numbering: { reference: 'bullets', level: 0 }, children: ri(t), ...sp(40, 40) });
const gap = () => new Paragraph({ children: [new TextRun('')], ...sp(0, 80) });

// Numbered list — nl(refName, items)
// items: string | { t: string } (sub-bullet)
const nl = (ref, items) => items.map(it =>
  typeof it === 'string'
    ? new Paragraph({ numbering: { reference: ref, level: 0 }, children: ri(it), ...sp(40, 40) })
    : new Paragraph({ numbering: { reference: 'sub', level: 0 }, children: ri(it.t), ...sp(20, 20) })
);

// Note / callout block (blockquote)
const nt = t => new Paragraph({
  children: ri(t),
  indent: { left: 540 },
  border: { left: { style: BorderStyle.THICK, size: 12, color: '2563EB', space: 8 } },
  ...sp(160, 160)
});

// Table helpers
const CB  = { style: BorderStyle.SINGLE, size: 1, color: 'D1D5DB' };
const CBs = { top: CB, bottom: CB, left: CB, right: CB };

function tbl(headers, rows, widths) {
  const cell = (text, w, isHdr) => new TableCell({
    borders: CBs,
    width: { size: w, type: WidthType.DXA },
    margins: { top: 100, bottom: 100, left: 140, right: 140 },
    shading: isHdr ? { fill: 'EFF6FF', type: ShadingType.CLEAR } : undefined,
    children: [new Paragraph({
      children: isHdr ? [new TextRun({ text, bold: true, size: 22 })] : ri(text),
      ...sp(20, 20)
    })]
  });
  return new Table({
    width: { size: CW, type: WidthType.DXA },
    columnWidths: widths,
    rows: [
      new TableRow({ tableHeader: true, children: headers.map((h, i) => cell(h, widths[i], true)) }),
      ...rows.map(row => new TableRow({ children: row.map((c, i) => cell(c, widths[i], false)) }))
    ]
  });
}

